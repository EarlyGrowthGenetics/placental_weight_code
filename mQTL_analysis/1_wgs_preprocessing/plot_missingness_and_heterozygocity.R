#\---------------------------------------------------- Description ----------------------------------------------------#
# Plots sample missingness and heterozygosity 
#/---------------------------------------------------------------------------------------------------------------------#

#\------------------------------------------------------ Library ------------------------------------------------------#
library(ggplot2)
#/---------------------------------------------------------------------------------------------------------------------#

#\----------------------------------------------------- Read data -----------------------------------------------------#
args = commandArgs(trailingOnly=TRUE)
miss <- read.table(args[1], header=T, sep="\t")
het <- read.table(args[2], header=T, sep="\t")
#/---------------------------------------------------------------------------------------------------------------------#

#\----------------------------------------------------- Plot data -----------------------------------------------------#
missPlot <- ggplot(miss) + geom_point(aes(x=1:length(F_MISS), y=F_MISS))
ggsave(plot = missPlot, filename = args[3])

hetPlot <- ggplot(het) + geom_point(aes(x=1:length(F), y=F))
ggsave(plot = hetPlot, filename = args[4])
#/---------------------------------------------------------------------------------------------------------------------#